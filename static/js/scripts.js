
$(document).ready(function() {
	$('#nextClick').click(function(){
		$('#prevClick').css("display","");
		$('#more-info').css("display","");
		$('#start-info').css("display","none");
		$('#nextClick').css("display","none");

	});
});

$(document).ready(function() {
	$('#prevClick').click(function(){
		$('#nextClick').css("display","");
		$('#more-info').css("display","none");
		$('#start-info').css("display","");
		$('#prevClick').css("display","none");

	});
});

